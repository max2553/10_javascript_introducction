function sum(x, y) {
    let ergebnis = x + y;
    return ergebnis;
}

function zweitessum(a, b) {
    let ergebniszwei = a * b;
    return ergebniszwei;
}

function sumdrei(d,e) {
    let ergebnisdrei = d / e;
    return ergebnisdrei;
}

sum(10, 2);
zweitessum(11,11);
sumdrei(100,10);

console.log("Ergebnis = " + sum(10,2));
console.log("Ergebniszwei = " + zweitessum(11,11));
console.log("Ergebnisdrei = " + sumdrei(100,10));
